#include <iostream>
#include <vector>

using namespace std;

int Min(int a, int b, int c)
{
    return min(min(a, b), c);
}

int main()
{
    int n; cin >> n;
    vector<int> a(n);
    for (int i = 0; i < n; ++i) cin >> a[i];

    int min_even, min_odd, min_curr_even, min_curr_odd;
    min_even = min_odd = min_curr_even = min_curr_odd = 1001;
    int min_product = 1001 * 1001;
    for (int i = 6; i < n; ++i) {
        int x = a[i - 6];
        int y = a[i];
        if (x % 2 == 0) {
            min_even = min(min_even, x);
        } else {
            min_odd = min(min_odd, x);
        }
        if (y % 2 == 0) {
            min_product = min(min_product, min(min_even, min_odd) * y);
        } else {
            min_product = min(min_product, min_even * y);
        }
    }

    if (min_product != 1001 * 1001) {
        cout << min_product << '\n';
    } else {
        cout << "-1\n";
    }
}
